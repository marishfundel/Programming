package m35_java_lang_classes;

public class MathClass {
    public static void main(String[] args) {

        int num1 = Math.max(100, 2000);
        System.out.println(num1);

        int num2 = Math.min(45, 67);
        System.out.println(num2);

        System.out.println(Math.abs(-100));

        double num3 = Math.pow(10, 4);
        System.out.println(num3);

        double num4 = Math.pow(5, 3);
        System.out.println(num4);

        double num5 = Math.sqrt(9);
        System.out.println(num5);

        System.out.println( Math.random());

        System.out.println(Math.round(15.2));
        System.out.println(Math.floor(12.9));
        System.out.println(Math.PI);

    }
}
