package m14_selection_statements_part1;

public class AssigningXY {

    public static void main(String[] args) {

        int x = 0, y;
        y = 20;
        x = 2;

        if (y == 20) {
            x = 5;
            System.out.println("x= " + x);
            System.out.println("y = " + y);
        } else {
            System.out.println("x= " + x);
            System.out.println("y = " + y);
        }



    }
}
